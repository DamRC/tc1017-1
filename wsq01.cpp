// Fun with numbers

#include <iostream>
using namespace std;

int main()
{
    cout << "Type in 2 values" << endl;
    int Number1;
    int Number2;
    
    cin >> Number1;
    cin >> Number2;
    
    cout << "Sum:" << Number1 + Number2 << endl;
    cout << "Difference:" << Number1 - Number2 << endl;
    cout << "Product:" << Number1 * Number2 << endl;
    cout << "Integer based division:" << Number1 / Number2 << endl;
    cout << "Remainder:" << Number1 % Number2 << endl;
    
}
